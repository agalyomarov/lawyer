                Право на защиту — преференция, предусмотренная в отношении каждого гражданина. Независимо от тяжести преступления, личности подозреваемого/обвиняемого, общественного резонанса, в процессе обязательно должен участвовать профессиональный защитник — адвокат по уголовным делам.</p>
            <p>Его роль трудно переоценить — зачастую от его квалификации зависит жизнь человека. Именно поэтому адвокатская коллегия Москвы «Почетный Адвокатъ» предлагает воспользоваться услугами состоящих в ней профессионалов.</p>
            <h2>Круг вопросов и основные направления работы</h2>
            <p>Участие профессионального защитника желательно с самых начальных стадий уголовного преследования. В некоторых случаях задержание гражданина может быть неправомерным, и исправить это сможет только квалифицированный специалист в лице адвоката/юриста по уголовным делам. Если по каким-то
                причинам не получилось обратиться к нему на стадии предъявления обвинения, то сделать это можно в любое другое время. Но лучше, конечно, обращаться как можно раньше. Краткий список рассматриваемых вопросов выглядит следующим образом:</p>
            <ol>
                <li>участие в процессе предъявления обвинения, и анализ правомерности задержания (и остальных процессуальных мер до возбуждения уголовного дела);</li>
                <li>ознакомление с материалами/подробностями уголовного дела;</li>
                <li>оспаривание любых мер пресечения/принуждения — арест; личный обыск; подписка о невыезде; помещение в СИЗО;</li>
                <li>участие в следственных действиях для проверки их соответствия процессуальным нормам (ознакомление с материалами следственных действий);</li>
                <li>ознакомление с обвинительным заключением и указание прокурору на допущенные следствием ошибки и нарушения;</li>
                <li>построение линии защиты — сбор доказательной базы; опросы свидетелей; систематизация недоработок следствия (если преступление связано с нарушением ПДД, то в связке с адвокатом может работать <a href="http://a-advokat.ru/advokat-po-dtp">юрист по ДТП</a>);</li>
                <li>судебное представительство вплоть до кассационной инстанции (услуги адвоката по уголовным делам распространяются и на обвиненных граждан).</li>
            </ol>
            <p>Соответственно, с момента задержания и до рассмотрения дела в суде, профессиональный защитник сделает все возможное для отстаивания интересов представляемого. Помимо практических мер, помощь может выражаться и в оказании квалифицированных юридических консультаций, которые оказываются
                опытным юристом по приемлемой стоимости.</p>
            <p>Цены на услуги адвоката зависят от сложности дела, поэтому специалисту изначально необходимо уточнить обстоятельства. Но Кодекс профессиональной этики, в первую очередь, указывает на доступность услуг, без отрицательного влияния на их качество. Если потребовался адвокат по уголовным
                делам в Москве, то коллегия «Почетный Адвокатъ» готова предложить свою помощь всем обратившимся.</p>
